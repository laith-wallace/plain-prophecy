// Build a fast lookup set of OSIS references that are in the Christ Thread
export const CHRIST_THREAD_REFS = new Set<string>()
for (const conn of CHRIST_THREAD) {
  CHRIST_THREAD_REFS.add(conn.from)
  CHRIST_THREAD_REFS.add(conn.to)
}

/**
 * Look up a ChristThreadConnection by its from/to OSIS references.
 * Returns null if not a curated Christ Thread connection.
 */
export function findChristThreadConnection(
  from: string,
  to: string
): ChristThreadConnection | null {
  return (
    CHRIST_THREAD.find((c) => c.from === from && c.to === to) ||
    CHRIST_THREAD.find((c) => c.from === to && c.to === from) ||
    null
  )
}
